	default boolean createV(AdvancedStoreHouse plugin, Object object) 
	{
		if(!(object instanceof TransferLog))
		{
			return false;
		}
		TransferLog cu = (TransferLog) object;
		PreparedStatement preparedStatement = null;
		Connection conn = plugin.getMysqlSetup().getConnection();
		if (conn != null) {
			try 
			{
				String sql = "INSERT INTO `" + plugin.getMysqlHandler().tableNameV 
						+ "`(`datum`, `distributionchestid`, `storagechestid`, `distributedcontent`) " 
						+ "VALUES(?, ?, ?, ?)";
				preparedStatement = conn.prepareStatement(sql);
				preparedStatement.setLong(1, cu.getDate());
		        preparedStatement.setInt(2, cu.getDistributionChestID());
		        preparedStatement.setLong(3, cu.getStorageChestID());
		        preparedStatement.setString(4, ConvertHandler.ToBase64itemStackArray(cu.getDistributedContent()));
		        
		        preparedStatement.executeUpdate();
		        return true;
		    } catch (SQLException e) 
			{
				  AdvancedStoreHouse.log.warning("Error: " + e.getMessage());
				  e.printStackTrace();
		    } finally 
			{
		    	  try 
		    	  {
		    		  if (preparedStatement != null) 
		    		  {
		    			  preparedStatement.close();
		    		  }
		    	  } catch (Exception e) 
		    	  {
		    		  e.printStackTrace();
		    	  }
		      }
		}
		return false;
	}
	
